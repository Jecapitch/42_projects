#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *newnode)
{
	t_list	*last;

	last = NULL;
	if (!lst || !newnode)
		return ;
	if (*lst)
	{
		last = ft_lstlast(*lst);
		last->next = newnode;
	}
	else
		*lst = newnode;
}
