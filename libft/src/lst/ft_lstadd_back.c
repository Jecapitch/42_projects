#include "libft.h"

void	ft_lstadd_back(t_list **lst, t_list *newnode)
{
	t_list	*last;

	if (!lst)
		return (ft_err_nonnull(NULL, -1, __func__));
	if (!newnode)
		return ;
	if (*lst)
	{
		last = ft_lstlast(*lst);
		last->next = newnode;
	}
	else
		*lst = newnode;
}
