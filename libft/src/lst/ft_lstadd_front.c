#include "libft.h"

void	ft_lstadd_front(t_list **lst, t_list *newnode)
{
	if (!lst)
		return (ft_err_nonnull(NULL, -1, __func__));
	if (!newnode)
		return ;
	newnode->next = *lst;
	*lst = newnode;
}
